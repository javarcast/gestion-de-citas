<template>
    <app-layout title="Usuarios">
        <template #header>
            <h2 class="font-semibold text-xl text-gray-800 leading-tight">
               <i class="fas fa-users"></i> Modulo de Usuarios
            </h2>
        </template>

        <div class="py-12">
            <div class="max-w-7xl mx-auto sm:px-6 lg:px-8">
                <div class="md:grid md:grid-cols-3 md:gap-6">
                    <div class="md:col-span-1">
                        <div class="px-4 sm:px0">
                            <h3 class="text-lg text-gray-900"> Listado de Usuarios</h3>
                            <p class="text-sm text-gray-600">Selecciona el Usuario correcto y ejecuta cualquier funci√≥n (ver, editar o eliminar)</p>
